package section3.F_Char_Bool;

public class CharBool {
    public static void main(String[] args){
        // single characters
        char myChar = 'M';
        char myUnicode = '\u00A9';
        boolean myBoolean = true;
        // or false
        System.out.println("myChar:\n\n\t"+ myChar + "\nmyBoolean:\n\n\t" + myBoolean);
        System.out.println("myUnicode:\n\n\t"+ myUnicode);
    }
}
