package section3.A_Variables;

public class Variables {
    
    public static void main(String[] args) {
        int myFirstInt = 15 * 3;
        int mySecondInt = 6;
        int myThirdInt = 12 * myFirstInt;
        int total = myFirstInt + mySecondInt + myThirdInt;
        int myLastInt = 1000 - total;
        System.out.println(myFirstInt);
        System.out.println(mySecondInt);
        System.out.println(myThirdInt);
        System.out.println(total);
        System.out.println(myLastInt);
    }
}
